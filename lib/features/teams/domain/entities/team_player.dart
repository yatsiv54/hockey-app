class TeamPlayer {
  const TeamPlayer({
    required this.id,
    required this.number,
    required this.name,
    required this.position,
    required this.shoots,
    required this.dob,
    required this.country,
  });

  final int id;
  final String number;
  final String name;
  final String position;
  final String shoots;
  final String dob;
  final String country;
}
